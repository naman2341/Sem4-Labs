; MULTIPLICATION OF 2 MATRICES.
.DATA
A: .WORD 1,2,3,4,5,6,7,8,9
B: .WORD 1,2,3,4,5,6,7,8,9
C: .WORD 0,0,0,0,0,0,0,0,0

.TEXT
	LDR R0,=A
	LDR R1,=B
	LDR R2,=C
    
	MOV R3,#0  ;INNER LOOP COUNT I INDEX
	MOV R4,#0  ;OUTER LOOP COUNT J INDEX
	MOV R10,#3 ; NUMBER OF ELEMENTS IN A ROW
	MOV R8,#0 ;VALUE OF K
	
LOOP1:MLA R11,R3,R10,R8
      MOV R11,R11,LSL #2
	  LDR R5,[R0,R11]
	  
	  MLA R12,R8,R10,R4
      MOV R12,R12,LSL #2
	  LDR R6,[R1,R12]
	  
	  MUL R11,R5,R6 ; REGISTER R11 IS REUSED.
	  ADD R9,R9,R11
	  
	  ADD R8,R8,#1    ; INCREMENT K  INNERMOST LOOP
	  CMP R8,#3
	  BNE LOOP1 
	  
	  MLA R12,R3,R10,R4     ; STORE THE IN C[I][J]
      MOV R12,R12,LSL #2
	  STR R9,[R2,R12]
	  
	  MOV R8,#0       ; K=0
	  MOV R9,#0       ; C[I][J]=0
	  ADD R4,R4,#1
	  CMP R4,#3
      BNE LOOP1
	  MOV R4,#0
	  ADD R3,R3,#1
	  CMP R3,#3
	  BNE LOOP1
	  SWI 0X011 
.END
	
	
	
	

